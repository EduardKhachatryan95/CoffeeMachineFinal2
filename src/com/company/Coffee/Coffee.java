package com.company.Coffee;

public  abstract class Coffee {
    Integer  water = null;
    Integer milk=null;
    Integer coffee=null;
    Integer money=null;

    public Integer getWater() {
        return water;
    }

    public Integer getMilk() {
        return milk;
    }

    public Integer getCoffee() { return coffee ; }

    public Integer getMoney() {
        return money;
    }
}
